#include "3-calc.h"

/**
 * op_add - return the sum of a and b
 * @a: first integer
 * @b: second integer
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return the difference of a and b
 * @a: first integer
 * @b: second integer
 * Return: differece
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the product of a and b
 * @a: first integer
 * @b: second integer
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return the result of div of a and b
 * @a: first integer
 * @b: second integer
 * Return: the result of division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the remainder of div of a and b
 * @a: first integer
 * @b: second integer
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
